function AddRoundKey(round, state, roundKey) {
    for (let i = 0; i < 4; i++) {
        for (let j = 0; j < 4; j++) {
            state[i][j] ^= roundKey[(round * Nb * 4) + (i * Nb) + j];
        }
    }
}

function InvShiftRows(state) {
    let temp;

    //Rotate first row 1 columns to right
    temp = state[3][1];
    state[3][1] = state[2][1];
    state[2][1] = state[1][1];
    state[1][1] = state[0][1];
    state[0][1] = temp;

    //Rotate second row 2 columns to right
    temp = state[0][2];
    state[0][2] = state[2][2];
    state[2][2] = temp;

    temp = state[1][2];
    state[1][2] = state[3][2];
    state[3][2] = temp;

    //Rotate third row 3 columns to right
    temp = state[0][3];
    state[0][3] = state[1][3];
    state[1][3] = state[2][3];
    state[2][3] = state[3][3];
    state[3][3] = temp;
}

function InvSubBytes(state) {
    for (let i = 0; i < 4; i++) {
        for (let j = 0; j < 4; j++) {
            state[i][j] = inv_s_box[state[i][j]];
        }
    }
}

function to2DArray(arr) {
    if (arr.length !== 16) {
        throw new Error("Input array must have 16 elements.");
    }
  
    let matrix = [];
    for (let i = 0; i < 4; i++) {
        matrix.push(arr.slice(i * 4, (i + 1) * 4));
    }
  
    return matrix;
}

function to1DArray(matrix) {
    let newArr = [];
    for(var i = 0; i < matrix.length; i++)
    {
        for (var j = 0; j < matrix[i].length; j++)
        {
          newArr.push(matrix[i][j]);
        }
    }
    return newArr;
}


function InvCipher(state, roundKey) {
    state = to2DArray(state);

    let round = 0;

    AddRoundKey(10, state, roundKey);

    for (round = 9; ; round--) {
        InvShiftRows(state);
        InvSubBytes(state);
        AddRoundKey(round, state, roundKey);
        if (round == 0) break;
        InvMixColumns(state);
    }
    state = to1DArray(state);
    return state;
}

function InvMixColumns(state) {
    for (let i = 0; i < 4; i++) {
        let a = state[i][0];
        let b = state[i][1];
        let c = state[i][2];
        let d = state[i][3];

        state[i][0] = mul(a, 0x0e) ^ mul(b, 0x0b) ^ mul(c, 0x0d) ^ mul(d, 0x09);
        state[i][1] = mul(a, 0x09) ^ mul(b, 0x0e) ^ mul(c, 0x0b) ^ mul(d, 0x0d);
        state[i][2] = mul(a, 0x0d) ^ mul(b, 0x09) ^ mul(c, 0x0e) ^ mul(d, 0x0b);
        state[i][3] = mul(a, 0x0b) ^ mul(b, 0x0d) ^ mul(c, 0x09) ^ mul(d, 0x0e);
    }
}

function mul(x, y) {
    return (
      ((y & 1) * x) ^
      ((y >> 1 & 1) * xtime(x)) ^
      ((y >> 2 & 1) * xtime(xtime(x))) ^
      ((y >> 3 & 1) * xtime(xtime(xtime(x)))) ^
      ((y >> 4 & 1) * xtime(xtime(xtime(xtime(x)))))
    );
}

function xtime(x) {
    return ((x << 1) ^ (((x >> 7) & 1) * 0x1b)) & 0xFF;
}
  

function decrypt(cipherText, key) {
    let plainText = '';
    const ctx = { RoundKey: new Uint8Array(176) };
    keyExpansion(ctx.RoundKey, key);
    for (let i = 0; i < 4; i++) {
        let plainText_i = InvCipher(cipherText.slice(i * 16, (i + 1) * 16), ctx.RoundKey); 
        plainText += phex(plainText_i);
    }
    return plainText;
}

function phex(data) {
    return Array.from(data).map(b => ('00' + b.toString(16)).slice(-2)).join(' ');
}

function RotWord(word) {
    let temp = word[0];
    word[0] = word[1];
    word[1] = word[2];
    word[2] = word[3];
    word[3] = temp;
}

function SubWord(word) {
    for (let i = 0; i < 4; i++) {
        word[i] = s_box[word[i]];
    }
}

function keyExpansion(roundKey, key) {
    let temp = new Array(4);
    let i, j, k;

    for (i = 0; i < Nk; i++) {
        roundKey[(i * 4) + 0] = key[(i * 4) + 0];
        roundKey[(i * 4) + 1] = key[(i * 4) + 1];
        roundKey[(i * 4) + 2] = key[(i * 4) + 2];
        roundKey[(i * 4) + 3] = key[(i * 4) + 3];
    }

    for (let i = Nk; i < Nb * (Nr + 1); i++) {
        for (let t = 0; t < 4; t++) {
            temp[t] = roundKey[(i - 1) * 4 + t];
        }
        if (i % Nk == 0) {
            RotWord(temp);
            SubWord(temp);
            temp[0] ^= Rcon[Math.floor(i / Nk)];
        }

        for (let t = 0; t < 4; t++) {
            roundKey[i * 4 + t] = roundKey[(i - Nk) * 4 + t] ^ temp[t];
        }
    }
}

function uint8ArrayToAsciiString(byteArray) {
    if (!(byteArray instanceof Uint8Array)) {
      throw new TypeError('Input must be a Uint8Array');
    }
    return String.fromCharCode.apply(null, byteArray);
}

function hexStringToUint8Array(hexString) {
    // Split the string by spaces to get individual hex values
    const hexValues = hexString.trim().split(/\s+/);
    
    // Convert each hex value to a number and store in a new Uint8Array
    const uint8Array = new Uint8Array(hexValues.map(hex => parseInt(hex, 16)));
    
    return uint8Array;
}

